The ostrich shares the order Struthioniformes with the kiwis, emus, rheas, and cassowaries. However, phylogenetic studies have shown that it is the basal extant member of Palaeognathae and is thus equally closely related to flighted tinamous.[3][4] It is distinctive in its appearance, with a long neck and legs, and can run at up to about 70 km/h (43 mph),[5] the fastest land speed of any bird.[6] The ostrich is the largest living species of bird and lays the largest eggs of any living bird (extinct elephant birds of Madagascar and the giant moa of New Zealand laid larger eggs).

